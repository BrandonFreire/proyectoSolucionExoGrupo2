/**
 * Interfaz que define métodos para enseñar aspectos del idioma.
 */
public interface PJIEnseniar {
    
    /**
     * Método para enseñar léxico del idioma.
     */
    void pjEnseniarLexico();

    /**
     * Método para enseñar gramática del idioma.
     */
    void pjEnseniarGramatica();

    /**
     * Método para enseñar fonética del idioma.
     */
    void pjEnseniarFonetica();
}
